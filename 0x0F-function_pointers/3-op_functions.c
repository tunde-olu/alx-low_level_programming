#include "3-calc.h"

/**
 * op_add - Add tw digit
 * @a: first digit
 * @b: second digit
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtact two digit
 * @a: first digit
 * @b: second digit
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiply two digit
 * @a: first digit
 * @b: second digit
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide two digit
 * @a: first digit
 * @b: second digit
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Remainder(modulo) of two digit
 * @a: first digit
 * @b: second digit
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}


